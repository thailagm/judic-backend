CREATE TABLE papel(
    id BIGINT NOT NULL,
    nome VARCHAR(255),
    CONSTRAINT pk_papel PRIMARY KEY(id)
);
CREATE SEQUENCE papel_id_seq
    INCREMENT BY 1
    MINVALUE 1
    MAXVALUE 9223372036854775807
    START 1;


